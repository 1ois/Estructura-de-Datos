    public void mostrarInOrden(Nodo p)
    {
        if(p != null)
        {
            mostrarInOrden(p.izq);
            txaResultado.append(p.info.nro + "\t" + p.info.nombre + "\n");
            mostrarInOrden(p.der);
        }
    }

